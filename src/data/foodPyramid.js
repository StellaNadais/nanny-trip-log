const SORTED_KEYS = Object.keys(FOOD_MAP).sort((a, b) => b.length - a.length)

export function lookupFoodCategory(token) {
  const normalized = token.toLowerCase().trim().replace(/\s+/g, ' ')
  if (!normalized) return 'unknown'

  if (FOOD_MAP[normalized]) return FOOD_MAP[normalized]

  const words = normalized.split(/\s+/).filter(Boolean)
  for (const w of words) {
    if (FOOD_MAP[w]) return FOOD_MAP[w]
  }

  let bestCat = null
  let bestLen = 0
  for (const key of SORTED_KEYS) {
    if (normalized.includes(key) && key.length >= bestLen) {
      bestCat = FOOD_MAP[key]
      bestLen = key.length
    }
  }

  return bestCat ?? 'unknown'
}

export function colorForCategory(categoryId) {
  return FOOD_CATEGORIES[categoryId]?.color ?? FOOD_CATEGORIES.unknown.color
}
